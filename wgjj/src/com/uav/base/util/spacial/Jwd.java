package com.uav.base.util.spacial;

public class Jwd
{
	private double jd;
	private double wd;
	
	public Jwd(){}
	
	public Jwd(double jd, double wd) {
		this.jd = jd;
		this.wd = wd;
	}
	public double getJd()
	{
		return jd;
	}
	public void setJd(double jd)
	{
		this.jd = jd;
	}
	public double getWd()
	{
		return wd;
	}
	public void setWd(double wd)
	{
		this.wd = wd;
	}
}
